    /**
     * @param args the command line arguments
     */
  public void quitarColor(ColorFicha color) {
        switch (color) {
            case ROJO:
                btnRojo.setVisible(false);
                break;
            case AMARILLO:
                btnAmarillo.setVisible(false);
                break;
            case VERDE:
                btnVerde.setVisible(false);
                break;
            case AZUL:
                btnAzul.setVisible(false);
                break;
            
        }
    }

    public void mostrarColor(ColorFicha color) {
        switch (color) {
            case ROJO:
                btnRojo.setVisible(true);
                break;
            case AMARILLO:
                btnAmarillo.setVisible(true);
                break;
            case VERDE:
                btnVerde.setVisible(true);
                break;
            case AZUL:
                btnAzul.setVisible(true);
                break;
        }
    }  

