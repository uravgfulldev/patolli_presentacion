    private void btnAzulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAzulActionPerformed
        // TODO add your handling code here:
        quitarColor(ColorFicha.AZUL);
        if(jugadorSelec.getColor()!=null){
            mostrarColor(jugadorSelec.getColor());
        }
       Jugador jug = partidaSelec.getJugadores().get(partidaSelec.getJugadores().indexOf(jugadorSelec));
       jugadorSelec = jug;
       jugadorSelec.setNombre(txtJugador.getText());
       jugadorSelec.setColor(ColorFicha.AZUL);
       partidaSelec.getJugadores().set(partidaSelec.getJugadores().indexOf(jug), jugadorSelec);
       partidaSelec.setJugadorTurno(jugadorSelec);
        
    }//GEN-LAST:event_btnAzulActionPerformed

